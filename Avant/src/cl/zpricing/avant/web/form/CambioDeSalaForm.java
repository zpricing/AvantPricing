package cl.zpricing.avant.web.form;

public class CambioDeSalaForm {
	private String complejo;
	private String salaSwap1;
	private String salaSwap2;
	private String fechaDesde;
	private String fechaHasta;
	
	
	public String getComplejo() {
		return complejo;
	}
	public void setComplejo(String complejo) {
		this.complejo = complejo;
	}
	public String getSalaSwap1() {
		return salaSwap1;
	}
	public void setSalaSwap1(String salaSwap1) {
		this.salaSwap1 = salaSwap1;
	}
	public String getSalaSwap2() {
		return salaSwap2;
	}
	public void setSalaSwap2(String salaSwap2) {
		this.salaSwap2 = salaSwap2;
	}
	public String getFechaDesde() {
		return fechaDesde;
	}
	public void setFechaDesde(String fechaDesde) {
		this.fechaDesde = fechaDesde;
	}
	public String getFechaHasta() {
		return fechaHasta;
	}
	public void setFechaHasta(String fechaHasta) {
		this.fechaHasta = fechaHasta;
	}
}
