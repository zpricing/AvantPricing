package cl.zpricing.avant.model.loadmanager;

public class LayoutAreaPorCategoria {
	private String strAreaCategory;
	private Integer asientosTotales;
	private Integer asientosHouseSeats;
	private Integer asientosSpecialSeats;
	
	public String getStrAreaCategory() {
		return strAreaCategory;
	}
	public void setStrAreaCategory(String strAreaCategory) {
		this.strAreaCategory = strAreaCategory;
	}
	public Integer getAsientosTotales() {
		return asientosTotales;
	}
	public void setAsientosTotales(Integer asientosTotales) {
		this.asientosTotales = asientosTotales;
	}
	public Integer getAsientosHouseSeats() {
		return asientosHouseSeats;
	}
	public void setAsientosHouseSeats(Integer asientosHouseSeats) {
		this.asientosHouseSeats = asientosHouseSeats;
	}
	public Integer getAsientosSpecialSeats() {
		return asientosSpecialSeats;
	}
	public void setAsientosSpecialSeats(Integer asientosSpecialSeats) {
		this.asientosSpecialSeats = asientosSpecialSeats;
	}
}
