package cl.zpricing.avant.model.loadmanager;

public class AreaCount {
	private Integer areaBytNum;
	private String strControl;
	private String codigoCategoriaArea;
	private Integer asientosDisponibles;
	private Integer asientosVendidos;
	private Integer asientosReservados;
	private Integer asientosCasa;
	
	public String getCodigoCategoriaArea() {
		return codigoCategoriaArea;
	}
	public void setCodigoCategoriaArea(String codigoCategoriaArea) {
		this.codigoCategoriaArea = codigoCategoriaArea;
	}
	public Integer getAsientosDisponibles() {
		return asientosDisponibles;
	}
	public void setAsientosDisponibles(Integer asientosDisponibles) {
		this.asientosDisponibles = asientosDisponibles;
	}
	public Integer getAsientosVendidos() {
		return asientosVendidos;
	}
	public void setAsientosVendidos(Integer asientosVendidos) {
		this.asientosVendidos = asientosVendidos;
	}
	public Integer getAsientosReservados() {
		return asientosReservados;
	}
	public void setAsientosReservados(Integer asientosReservados) {
		this.asientosReservados = asientosReservados;
	}
	public Integer getAsientosCasa() {
		return asientosCasa;
	}
	public void setAsientosCasa(Integer asientosCasa) {
		this.asientosCasa = asientosCasa;
	}
	public Integer getAreaBytNum() {
		return areaBytNum;
	}
	public void setAreaBytNum(Integer areaBytNum) {
		this.areaBytNum = areaBytNum;
	}
	public String getStrControl() {
		return strControl;
	}
	public void setStrControl(String strControl) {
		this.strControl = strControl;
	}
}
